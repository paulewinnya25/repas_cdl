// Fonctions utilitaires pour la gestion des employés
export const getEmployeesByStatus = (status: string) => {
  return allEmployees.filter(emp => emp.statut_dossier === status);
};

export const searchEmployeesByName = (searchTerm: string) => {
  return allEmployees.filter(emp => 
    emp.nom_prenom.toLowerCase().includes(searchTerm.toLowerCase())
  );
};

export const getEmployeesByPosition = (position: string) => {
  return allEmployees.filter(emp => 
    emp.poste_actuel?.toLowerCase().includes(position.toLowerCase())
  );
};

export const getEmployeesByContractType = (contractType: string) => {
  return allEmployees.filter(emp => emp.type_contrat === contractType);
};

export const getEmployeesByNationality = (nationality: string) => {
  return allEmployees.filter(emp => emp.nationalite === nationality);
};

export const getEmployeesByGender = (gender: string) => {
  return allEmployees.filter(emp => emp.genre === gender);
};

export const getEmployeesByAgeRange = (minAge: number, maxAge: number) => {
  return allEmployees.filter(emp => 
    emp.age && emp.age >= minAge && emp.age <= maxAge
  );
};

export const getEmployeesBySalaryRange = (minSalary: number, maxSalary: number) => {
  return allEmployees.filter(emp => 
    emp.salaire_base && emp.salaire_base >= minSalary && emp.salaire_base <= maxSalary
  );
};

